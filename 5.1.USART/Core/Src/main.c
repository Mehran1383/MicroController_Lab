void uart_send(const char* msg)
{
	HAL_UART_Transmit(&huart1, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
}

void uart_receive_line(char* buf, uint16_t max_len)
{
	uint16_t idx = 0;
	uint8_t ch;
	
	while (idx < max_len - 1) {
		if (HAL_UART_Receive(&huart1, &ch, 1, HAL_MAX_DELAY) == HAL_OK) {
			if (ch == '\r' || ch == '\n')
				break;
			buf[idx++] = (char)ch;
		}
	}
	buf[idx] = '\0';
}

